"use strict";
// solicitamos ingresar datos dos numeros y realizar 
// las 4 operaciones basicas

let a = Number(prompt("Ingresar el primer número: "));
let b = Number(prompt("Ingresar el siguiente número: "));

let suma = a + b;
alert("El resultado de la suma es: " + suma);

let resta = a-b;
alert("El resultado de la resta es: " + resta);

let proc = a * b;
alert("El resultado del producto es: " + proc);

let div = a / b;
alert("El resultado de la división es: " + div);

let res = a % b;
alert("El resultado del residuo es: " + res);
